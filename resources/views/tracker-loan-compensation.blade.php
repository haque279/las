                                                    <i class="fas fa-minus acc-icon-plus"></i><i class="fas fa-plus acc-icon-minus"></i> How will I know if I am affected by the Examination?
                                                </div>
                                                <div class="answer">
                                                    <p>
                                                        Your lender will contact you if you are affected by the Examination. As the Examination covers all mortgage accounts that had, or may have had, an entitlement to a tracker rate, you don’t need to take any action in order to be added to the Examination’s scope.
                                                    </p>
                                                    <p>Under the Framework set down by the Central Bank for conducting the Examination, lenders are required to write to affected customers when they are identified and rectify their mortgage interest rate to prevent further detrimen</p>
